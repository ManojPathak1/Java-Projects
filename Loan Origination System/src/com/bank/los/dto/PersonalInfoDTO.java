package com.bank.los.dto;

public class PersonalInfoDTO {

	private String voterIDNo;
	private String licenseNo;
	private String panNo;
	private double assetValue;
	private String qualification;
	private String typeOfJob;
	private double incomePA;
	public String getVoterIDNo() {
		return voterIDNo;
	}
	public void setVoterIDNo(String voterIDNo) {
		this.voterIDNo = voterIDNo;
	}
	public String getLicenseNo() {
		return licenseNo;
	}
	public void setLicenseNo(String licenseNo) {
		this.licenseNo = licenseNo;
	}
	public String getPanNo() {
		return panNo;
	}
	public void setPanNo(String panNo) {
		this.panNo = panNo;
	}
	public double getAssetValue() {
		return assetValue;
	}
	public void setAssetValue(double assetValue) {
		this.assetValue = assetValue;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getTypeOfJob() {
		return typeOfJob;
	}
	public void setTypeOfJob(String typeOfJob) {
		this.typeOfJob = typeOfJob;
	}
	public double getIncomePA() {
		return incomePA;
	}
	public void setIncomePA(double incomePA) {
		this.incomePA = incomePA;
	}
	
		
	
}
